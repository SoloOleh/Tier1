def factorial(n):
    if n <= 1:
        return 1
    else:
        return n * factorial(n - 1)

factorial(5)    # 120

#Наприклад: 5! = 1 · 2 · 3 · 4 · 5 = 120. Це саме визначення можна записати рекурсивно:
# 5! = 5 · 4!
# 4! = 4 · 3!
# 3! = 3 · 2!
# 2! = 2 · 1!
# 1! = 1
print(factorial(6)) # цікаво що прінт можна задавати любе число функція обрахує